// if / else if / else

let marks = 75;

if (marks >= 90) {
  console.log("Grade: A");
} else if (marks >= 75) {
  console.log("Grade: B");
} else {
  console.log("Grade: C");
}

// Additional example
let temperature = 30;

if (temperature > 35) {
  console.log("It's hot");
} else if (temperature > 20) {
  console.log("Normal weather");
} else {
  console.log("Cold");
}
